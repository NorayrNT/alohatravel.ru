    public function updating(User $user) {

        // Reordering images if it doesn't start with index 0
        $all_images = $user->images;
        $index = '0';
        $data = [];
        if(is_array($all_images) && !empty($all_images)) {
            foreach($all_images as $k => $val) {
                if($index == '0' && $k !== '0') {
                    foreach($all_images as $image) {
                        $data[] = $image;
                    }
                    $user->images = $data;
                }
                break;
            }
        }
    }

